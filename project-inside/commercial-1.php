            $.ajax({
                url: actionUrl,
                data: $(this).serialize(),
                type: 'POST',
                beforeSend: function () {
                    $("#form-wait").show();
                    //$("#submit-enquiry").hide();
                },
                success: function(response) {
                    if (response) {
                        $("#form-wait").hide();
                        $('#enquiry-form').trigger("reset");
                        //$('#thank-you-msg').show();
                        const data = response;
                        downloadPDFFile(data.pdf,data.file_name);
                        $.magnificPopup.close();
                        setTimeout(() => {
                            $("[href='#test-popup1']").trigger('click');
                        }, 1000);
                        // close the pop-up
                        // setTimeout(function() {
